    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        //On Exit
        // update seed table
        String sql = "UPDATE seed set value = ? where id = 0 ";
 
        PreparedStatement statement;
        try {
            //making query
            statement = form.myConn.prepareStatement(sql);
            statement.setInt(1, listData.getSeed());

            int rowsInserted = statement.executeUpdate();
            if (rowsInserted > 0) {
                System.out.println("Seed was update successfully!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(viewPassenger.class.getName()).log(Level.SEVERE, null, ex);
        }
        int confirmed = JOptionPane.showConfirmDialog(null, 
        "Are you sure you want to exit the program?", "Exit Program Message Box",
        JOptionPane.YES_NO_OPTION);

        if (confirmed == JOptionPane.YES_OPTION) {

           //closing connection to DB
            if (myStmt != null) {
                try {
                    myStmt.close();
                } catch (SQLException ex) {
                    Logger.getLogger(form.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

            if (myConn != null) {
                try {
                    myConn.close();
                } catch (SQLException ex) {
                    Logger.getLogger(form.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            dispose();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

