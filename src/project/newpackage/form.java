    public form() throws SQLException {
//        listData = new  list();
        initComponents();
        centreWindow();//adjust ui to center
        if(dataLoaded == 0){// if database is no loaded
            try{
                //conting to data base
                myConn = DriverManager.getConnection("jdbc:mysql://localhost:3306/project", "root", "");

                    // 2. Create a statement
                myStmt = myConn.createStatement();
                // getting seed
                myRs = myStmt.executeQuery("select value from seed where id = 0 ");
                int seed = -1;
                 while (myRs.next()) {
                    seed = (myRs.getInt("value") );
                }
                if(seed != -1){//if seed is not empty
                    listData.setSeed(seed);
                }
                //getting people data
                myRs = myStmt.executeQuery("select * from people");
                 while (myRs.next()) {
                    People people = new People(myRs.getString("firstName") ,myRs.getString("lastName"),myRs.getString("passNumber"),
                            myRs.getString("date") , myRs.getInt("level"));
                    listData.insertData(people,myRs.getInt("id"));
                }
            }catch (Exception exc) {
                exc.printStackTrace();
            }
            dataLoaded = 1; // update flag
        }
        
    }
    
